/**
 * _abs - Computes the absolute value
 * of an integer.
 *
 * @i: input number as an integer.
 * Description: int _abs takes a parament n
 * Return: absolute value n
 */
int _abs(int n) /* * Description: int _abs takes a parament n*/
{
if (n >= 0)
{
return (n);
}
else
{
return (n * -1);
}
}
